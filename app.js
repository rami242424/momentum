const calculator = {
    add: function(a, b){
        console.log(a + b);
    },
    minus: function(a, b){
        console.log(a - b);
    },
    divide: function(a, b){
        console.log(a / b);
    },
    multiply: function(a, b){
        console.log(a * b);
    },
    powerof: function(a, b){
        console.log(a ** b);
    },
};

calculator.add(5, 1); 
calculator.minus(5, 1); 
calculator.divide(15, 5); 
calculator.multiply(5, 3); 
calculator.powerof(5, 2); 

// const  : 이 친구는 variable을 만들 것이니 준비해야겠군
// 1. 변수 없이 작성시
console.log(5 + 2); // 7
console.log(5 * 2); // 10
console.log(5 / 2); // 2.5

// 2. 변수로 작성시
const a = 5;
const b = 2;
const myName = "rami";

console.log(a + b); // 7
console.log(a * b); // 10
console.log(a / b); // 2.5
console.log("hello " + myName); // hello rami